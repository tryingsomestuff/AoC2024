def get_sides(region):
   
    count = 0

    # Count left and right 'sides'
    previous_s = None
    previous_e = None
    for row in region:
        if '.' in row:
            line = ''.join(row)
            matches = get_match(line)
            s = []
            e = []
            for m in matches:
                s.append(m[0])
                e.append(m[1])
            #print(s)
            #print(e)
            for s_i in s:
                if not previous_s or not s_i in previous_s:
                    count +=1
            for e_i in e:
                if not previous_e or not e_i in previous_e:
                    count +=1
            previous_s = s
            previous_e = e

    # transpose
    region = list(map(list, zip(*region)))

    # Count top and bottom 'sides'
    previous_s = None
    previous_e = None
    for row in region:
        if '.' in row:
            line = ''.join(row)
            matches = get_match(line)
            s = []
            e = []
            for m in matches:
                s.append(m[0])
                e.append(m[1])
            #print(s)
            #print(e)
            for s_i in s:
                if not previous_s or not s_i in previous_s:
                    count +=1
            for e_i in e:
                if not previous_e or not e_i in previous_e:
                    count +=1
            previous_s = s
            previous_e = e

    #print(count)

    return count


def fence_price(garden_map):
    rows = len(garden_map)
    cols = len(garden_map[0])

    visited = [[False for _ in range(cols)] for _ in range(rows)]

    def bfs(start_row, start_col):
        plant_type = garden_map[start_row][start_col]
        queue = deque([(start_row, start_col)])
        visited[start_row][start_col] = True

        area = 0
        region = copy.deepcopy(garden_map)

        directions = [(-1, 0), (1, 0), (0, -1), (0, 1)]

        while queue:
            row, col = queue.popleft()
            area += 1
            region[row][col] = '.'

            for i, (dr, dc) in enumerate(directions):
                nr, nc = row + dr, col + dc

                if 0 <= nr < rows and 0 <= nc < cols:
                    if garden_map[nr][nc] == plant_type and not visited[nr][nc]:
                        visited[nr][nc] = True
                        queue.append((nr, nc))

        return area,region

    total_price = 0

    for r in range(rows):
        for c in range(cols):
            if not visited[r][c]:
                area, region = bfs(r, c)
                sides = get_sides(region)
                #for l in region:
                #    print(l)
                #print('====================================')
                total_price += area * sides

    return total_price

